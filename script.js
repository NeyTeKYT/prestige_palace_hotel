// Ouverture du menu de navigation
function openNav() {
  document.getElementById("mySidenav").style.width = "250px";
  document.getElementById("menu-lateral-deroulant-span").style.visibility = "hidden";
}

// Fermeture du menu de navigation
function closeNav() {
  document.getElementById("mySidenav").style.width = "0";
  document.getElementById("menu-lateral-deroulant-span").style.visibility = "visible";
}

